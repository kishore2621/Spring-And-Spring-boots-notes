package com.shopping.account;

public class Account 
{
  long acc;
  double amt;
  String name;
public long getAcc() {
	return acc;
}
public void setAcc(long acc) {
	this.acc = acc;
}
public double getAmt() {
	return amt;
}
public void setAmt(double amt) {
	this.amt = amt;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}

}
